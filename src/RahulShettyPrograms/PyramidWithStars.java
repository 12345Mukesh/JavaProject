package RahulShettyPrograms;

public class PyramidWithStars 
{

	public static void main(String args[])
	{
		
		for(int i=0;i<5;i++)  //outer loop
		{
			
			for(int j=0;j<=i;j++)     //printing stars based on rows
			{
				System.out.print(" * ");
				
				System.out.print("\t");
				
				
			}
			System.out.println(" ");
			
		}
		
		
		
		
		
		
		
		
		
		
		
		
		
	}
	
	
	
	
	
	
	
	
	
}
